public class InOutPorts {
    int inport;
    int outport;

    public InOutPorts() {
        inport = 0;
        outport = 0;
    }

    public int getInport() {
        return inport;
    }

    public void setInport(int inport) {
        this.inport = inport;
    }

    public int getOutport() {
        return outport;
    }

    public void setOutport(int outport) {
        this.outport = outport;
    }
}